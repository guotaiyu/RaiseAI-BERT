def convert_examples_to_features(examples, label_list, max_seq_length,
                                 tokenizer):
  """Convert a set of `InputExample`s to a list of `InputFeatures`."""

  features = []
  for (ex_index, example) in enumerate(examples):
    if ex_index % 10000 == 0:
      tf.logging.info("Writing example %d of %d" % (ex_index, len(examples)))

    feature = convert_single_example(ex_index, example, label_list,
                                     max_seq_length, tokenizer)

    features.append(feature)
  return features

def initialize_estimator():
    processor = MnliProcessor()
    label_list = processor.get_labels()
    
    tokenizer = tokenization.FullTokenizer(vocab_file=vocab_file, do_lower_case=True)
    
    is_per_host = tf.contrib.tpu.InputPipelineConfig.PER_HOST_V2
    run_config = tf.contrib.tpu.RunConfig(
        cluster=None,
        master=None,
        model_dir=output_dir,
        save_checkpoints_steps=1000,
        tpu_config=tf.contrib.tpu.TPUConfig(
            iterations_per_loop=1000,
            num_shards=8,
            per_host_input_for_training=is_per_host))
    
    model_fn = model_fn_builder(
        bert_config=bert_config,
        num_labels=len(label_list),
        init_checkpoint=init_checkpoint,
        learning_rate=5e-5,
        num_train_steps=None,
        num_warmup_steps=None,
        use_tpu=False,
        use_one_hot_embeddings=False)
    
    estimator = tf.contrib.tpu.TPUEstimator(
        use_tpu=False,
        model_fn=model_fn,
        config=run_config,
        train_batch_size=32,
        eval_batch_size=8,
        predict_batch_size=predict_batch_size)

    return label_list, tokenizer, estimator

label_list, tokenizer, estimator = initialize_estimator()

def predict(sentence1, sentence2):
    #1
    input_examples = [InputExample(guid="", text_a = sentence1, text_b = sentence2, label = "contradiction")] # here, "" is just a dummy label
    
    #2
    input_features = convert_examples_to_features(input_examples, label_list, max_seq_length, tokenizer)
    
    #3
    predict_input_fn = input_fn_builder(features=input_features, seq_length=max_seq_length, is_training=False, drop_remainder=False)
    
    #4
    predictions = estimator.predict(input_fn=predict_input_fn)
    
    values = map(str, list(predictions)[0]['probabilities'])
    keys = ["contradiction", "entailment", "neutral"]
    
    return dict(zip(keys, values))
